#include <iostream>

int main()
{
    // Option 1: Combined declaration and assignment
    int i = 0;

    // Option 2: Separate declaration and assignment
    int j;
    j = 0;

    // Print the values
    std::cout << "i = " << i << std::endl;
    std::cout << "j = " << j << std::endl;

    return 0;
}
